				&#8203;<br><strong>Q: Apakah Kasbon?</strong><br>
				A: Kasbon adalah pinjaman online pertama di Indonesia, cepat dan terpercaya.&nbsp; Kasbon memberikan pinjaman tanpa jaminan, tanpa potongan dan tanpa penalti dalam jangka pendek yang diproses secara online untuk keperluan konsumtif maupun usaha.&nbsp; &nbsp; Bertujuan menyediakan proses pengajuan pinjaman secara online, sehingga Anda bisa mengakses dimana saja dan kapan saja, sebagai alternatif dari model pinjaman konvensional bank atau perusahaan pembiayaan lainnya.<br>
				<br>
				<strong>Q: Permohonan apa saja yang dapat diajukan untuk meminjam di Kasbon? </strong><br>
				A:&nbsp; Permohonan dapat diajukan unutk memenuhi segala macam kebutuhan konsumtif, kesehatan, pendidikan, berlibur, membeli perabotan baru atau sebagai tambahan membeli kendaraan, dan kebutuhan lainnya.<br>
				<br>
				<strong>Q: Apakah pengajuan pinjaman di Kasbon memerlukan jaminan atau kartu kredit?</strong><br>
				A:&nbsp; Tidak. Pinjaman di Kasbon adalah pinjaman tanpa jaminan dan tanpa persyaratan kartu kredit<br>
