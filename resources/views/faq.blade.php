				&nbsp;<br>
								<strong>Q: Bagaimana sistem pengembalian pinjaman?</strong><br>
				A:&nbsp; Sistem pengembalian pinjaman yang diberlakukan oleh Kasbon adalah sistem pembayaran 1 (satu) kali, pada hari tanggal jatuh tempo harus melunasi pokok pinjaman dan kewajiban biaya layanan.<br>
				&nbsp;&nbsp;&nbsp;&nbsp; Total tagihan sesuai dengan simulasi perhitungan kredit awal yang diinformasikan kepada nasabah saat pengajuan.<br>
				&nbsp;&nbsp;&nbsp;&nbsp; Jumlah total tagihan yang perlu dilunasi dapat dilihat pada halaman dashboard nasabah.<br>
				<br>
				&nbsp;&nbsp; &nbsp; Contoh :<br>
				&nbsp;&nbsp;&nbsp;&nbsp; Untuk pinjaman Rp 2.000.000,- jangka waktu 10 hari adalah sebagai berikut :<br>
				&nbsp;<br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Total biaya layanan sebesar :<br>
				&nbsp;&nbsp; &nbsp;&nbsp; Rp 2.000.000,- x ((1+1%)<sup>10</sup>) = Rp 209.244,-<br>
				&nbsp;<br>
				&nbsp;&nbsp;&nbsp;&nbsp; Sehingga total tagihan yang harus dibayarkan di hari ke-10 adalah Rp 2.209.244,-<br>
