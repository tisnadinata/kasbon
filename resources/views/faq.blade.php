				<strong>Q: Berapa lama proses pengajuan pinjaman di Kasbon?</strong><br>
				A:&nbsp; Lamanya proses pengajuan adalah 3 (tiga) hari kerja, sejak Anda mengajukan online, verifikasi sampai dengan pencairan dana pinjaman Anda. Lamanya proses juga bergantung pada kelengkapan syarat dan mudah atau tidaknya Anda dihubungi.<br>
